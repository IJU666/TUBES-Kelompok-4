    private void tableRencanaBudgetMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableRencanaBudgetMouseClicked
        int baris = tableRencanaBudget.rowAtPoint(evt.getPoint());
        String id = tableRencanaBudget.getValueAt(baris, 0).toString();
        formId.setText(id);
        String kategori = tableRencanaBudget.getValueAt(baris, 1).toString();
        cb_rb.setSelectedItem(kategori);
        String jumlahRencanaBudget = tableRencanaBudget.getValueAt(baris, 2).toString();
        formJumlah.setText(String.valueOf(jumlahRencanaBudget));
        String tglAwal = tableRencanaBudget.getValueAt(baris, 3).toString();
        formTanggalAwal.setText(tglAwal);
        String tglAkhir = tableRencanaBudget.getValueAt(baris, 4).toString();
        formTanggalAkhir.setText(tglAkhir);
        String bulan = tableRencanaBudget.getValueAt(baris, 5).toString();
        formBulan.setText(bulan);
    }//GEN-LAST:event_tableRencanaBudgetMouseClicked

