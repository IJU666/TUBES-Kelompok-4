            private void readRencanaBudget(){
         DefaultTableModel model = new DefaultTableModel();
            model.addColumn("ID Rencana Budget");
            model.addColumn("Kategori");
            model.addColumn("Jumlah Pemasukan");
            model.addColumn("Tanggal Awal");
            model.addColumn("Tanggal Akhir");
            model.addColumn("Bulan");

        try {
            String sql = "SELECT * FROM `rencanabudget`";
            java.sql.Connection conn = new koneksiDatabase().configDB();
            java.sql.Statement stm = conn.createStatement();
            java.sql.ResultSet res = stm.executeQuery(sql);
            while (res.next()) {            
            model.addRow(new Object[]{res.getString(1),res.getString(2),res.getString(3), res.getString(5), res.getString(6), res.getString(4)});
        }
            tableRencanaBudget.setModel(model);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e);
        }
    }
     
