    private void tablePengeluaranMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablePengeluaranMouseClicked
        int baris = tablePengeluaran.rowAtPoint(evt.getPoint());
        String id = tablePengeluaran.getValueAt(baris, 0).toString();
        formId.setText(id);
        String catatan = tablePengeluaran.getValueAt(baris, 1).toString();
        formCatatan.setText(catatan);
        
        String tanggalPengeluaran = tablePengeluaran.getValueAt(baris, 3).toString();
        formTanggal.setText(tanggalPengeluaran);
        
        String bulanPengeluaran = tablePengeluaran.getValueAt(baris, 4).toString();
        formBulan.setText(bulanPengeluaran);
        
        String kategoriPengeluaran = tablePengeluaran.getValueAt(baris, 2).toString();
        cb_pengeluaran.setSelectedItem(kategoriPengeluaran);
        
        String jumlah = tablePengeluaran.getValueAt(baris, 5).toString();
        formJumlah.setText(String.valueOf(jumlah));
    }//GEN-LAST:event_tablePengeluaranMouseClicked

