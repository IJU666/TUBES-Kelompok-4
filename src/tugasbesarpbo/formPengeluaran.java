       private void readPengeluaran(){
         DefaultTableModel model = new DefaultTableModel();
            model.addColumn("ID Pengeluaran");
            model.addColumn("Catatan");
            model.addColumn("Kategori");
            model.addColumn("Tanggal");
            model.addColumn("Bulan");
            model.addColumn("Jumlah Pemasukan");

        try {
            String sql = "SELECT * FROM `pengeluaran`";
            java.sql.Connection conn = new koneksiDatabase().configDB();
            java.sql.Statement stm = conn.createStatement();
            java.sql.ResultSet res = stm.executeQuery(sql);
            while (res.next()) {            
            model.addRow(new Object[]{res.getString(1),res.getString(2),res.getString(4), res.getString(5), res.getString(3), res.getString(6)});
        }
            tablePengeluaran.setModel(model);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e);
        }
    }
