    private void tableDanaDaruratMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableDanaDaruratMouseClicked
         int baris = tableDanaDarurat.rowAtPoint(evt.getPoint());
     String id = tableDanaDarurat.getValueAt(baris, 0).toString();
     formId.setText(id);
     String catatan = tableDanaDarurat.getValueAt(baris, 1).toString();
     formCatatan.setText(catatan);
     String tanggal = tableDanaDarurat.getValueAt(baris, 2).toString();
     formTanggal.setText(tanggal);
     String bulan = tableDanaDarurat.getValueAt(baris, 3).toString();
     formBulan.setText(bulan);
        String jumlahPemasukan = tableDanaDarurat.getValueAt(baris, 4).toString();
     formJumlah.setText(jumlahPemasukan);
    }//GEN-LAST:event_tableDanaDaruratMouseClicked

