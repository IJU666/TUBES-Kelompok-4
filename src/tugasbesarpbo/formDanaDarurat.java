     private void readPemasukan(){
         DefaultTableModel model = new DefaultTableModel();
            model.addColumn("ID Dana Darurat");
            model.addColumn("Catatan");
            model.addColumn("Tanggal masuk dana");
            model.addColumn("Bulan masuk dana");
            model.addColumn("Jumlah Dana Darurat");

        try {
            String sql = "SELECT * FROM `danaDarurat`";
            java.sql.Connection conn = new koneksiDatabase().configDB();
            java.sql.Statement stm = conn.createStatement();
            java.sql.ResultSet res = stm.executeQuery(sql);
            while (res.next()) {            
            model.addRow(new Object[]{res.getString(1),res.getString(2),res.getString(4), res.getString(3), res.getString(5)});
        }
            tableDanaDarurat.setModel(model);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e);
        }
    }
